# In Ruby, there are different *types* of data:

# Numbers

=begin

puts 2
puts 5

puts 5+2
puts 5*2
puts 5 / 2.0

=end

# Non-numbers

=begin

puts "Hello World"

puts "Tacos are" + "good"

puts "tacos" * 3

puts "test" + 8.to_s #to_s turns it into a string



# True, False

puts true 
puts false

# Nothing

puts nil



# Variables

food = "tacos"
quantity = 3
puts food


# Combine strings and variables

puts "tacos: #{quantity}"

=end

# String manipulation

puts "hello".length
puts "hello everyone".capitalize
puts "computer".reverse

creed = "This is the Way"

puts creed.upcase
puts creed.downcase
puts creed.swapcase
